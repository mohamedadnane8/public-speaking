export function getRandomWord(usedWords: string[] = []): string {
  // Filter out used words
  const availableWords = WORDS.filter(word => !usedWords.includes(word));
  
  // If all words have been used, reset and use all words
  const wordsToPickFrom = availableWords.length > 0 ? availableWords : WORDS;
  
  const randomIndex = Math.floor(Math.random() * wordsToPickFrom.length);
  return wordsToPickFrom[randomIndex];
}
